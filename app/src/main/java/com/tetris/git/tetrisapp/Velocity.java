package com.tetris.git.tetrisapp;

public class Velocity {

    float dx;
    float dy;

    public Velocity() {
    }

    public float getDx() {
        return dx;
    }

    public void setDx(float dx) {
        this.dx = dx;
    }

    public float getDy() {
        return dy;
    }

    public void setDy(float dy) {
        this.dy = dy;
    }



}
